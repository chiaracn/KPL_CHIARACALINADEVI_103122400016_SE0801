function jumlahDariSatuSampaiN(N) {
    return N * (N + 1) / 2;
}

let input1 = 5;
let input2 = 12;
let input3 = 30;
let input4 = 200;

console.log("Input:", input1, "Output:", jumlahDariSatuSampaiN(input1));
console.log("Input:", input2, "Output:", jumlahDariSatuSampaiN(input2));
console.log("Input:", input3, "Output:", jumlahDariSatuSampaiN(input3));
console.log("Input:", input4, "Output:", jumlahDariSatuSampaiN(input4));